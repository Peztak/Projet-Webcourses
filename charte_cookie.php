<p><h1><b><u>Charte des Cookies</u></b></h1>
<br/>Soucieux de la protection de votre vie privée, nous souhaitons, par la présente charte des cookies, 
<br/>informer les internautes utilisant le site Internet Webcourses du dépôt et de l’utilisation de cookies lors de la navigation sur le Site et des moyens mis à leur disposition pour paramétrer le fonctionnement des cookies.
<br/><h2><b><u>QU'EST CE QU'UN COOKIES ?</u></b></h2>
<br/><u>Définition des cookies:</u>
<br/>Le cookie se définit comme un fichier enregistré par l’outil de navigation de la machine dans un espace de stockage dédié.
<br/>Il est stocké au cours de la consultation d’un site web.
<br/>Son enregistrement dans le disque dur ne donne aucunement lieu à la divulgation d’informations personnelles concernant l’internaute.
<br/>Grâce à ce fichier texte, le site web peut identifier le terminal dans lequel il est déposé et mémoriser les informations nécessaires à son parcours.
<br/>Ce sont des fichiers uniques. Pour répondre à la question de comment savoir si un site utilise des cookies, il suffit d’ouvrir le site et un message d’avertissement apparaît.
<br/>Cette fenêtre, aussi appelée pop up de cookies, permet aux internautes de consentir à l’utilisation de cookies à chaque visite sur la plateforme. Elle n’apparaît généralement qu’une seule fois.
<br/><u>Mode de fonctionnement des cookies</u>
<br/>Les cookies n’identifient pas directement un utilisateur puisqu’ils ne contiennent ni nom ni prénom.
<br/>Le navigateur du terminal s’en charge. Par ailleurs, le terme « cookies » peut désigner des technologies différentes, à l’instar des balises ou images numériques invisibles intégrées sur les pages web ou dans les courriels.
<br/>En règle générale, la durée de vie d’un cookie est de treize mois au maximum après son enregistrement dans l’espace de stockage de la machine.
<br/>Elle ne peut être prolongée au cours des nouvelles visites sur la plateforme.
<br/>Les plateformes en ligne récoltent les informations sur les internautes à l’aide de leur adresse IP et de leurs cookies.
<br/>L’IP permet de déterminer d’où proviennent les visiteurs, qu’il s’agisse de la ville, de la région ou du pays.
<br/>Pour assurer la régularité du mode de fonctionnement des cookies, la Commission nationale de l’informatique et des libertés ou CNIL a établi une loi aidant à renforcer la protection des visiteurs de sites web.
<h2><b><u>À QUOI SERVENT LES COOKIES ?</u></b></h2>
<br/>À la base, les cookies servent à mémoriser des informations renseignées dans un formulaire rempli sur le site.
<br/>Ces renseignements peuvent être le nom, l’adresse et le mot de passe de l’utilisateur.
<br/>De cette manière, ce dernier n’aura plus à entrer ces informations lors de sa prochaine visite.
<br/>Il reste le seul à pouvoir accéder au fichier cookie.
<br/>Certains cookies peuvent stocker un panier d’achat, tandis que d’autres permettent d’enregistrer d’autres paramètres, telles que la langue du site ou la publicité ciblée.
<br/><u>Pour mieux appréhender l’utilité des cookies, il est nécessaire d’en distinguer les variantes utilisés par notre site, notamment les cookies :</u>
<br/>Techniques ;
<br/>De mesure d'audience ;
<br/>De partage ;
<br/>Strictement  nécessaires.
<br/><u>Les cookies techniques :</u>
<br/>Ce type de cookies permet aux internautes d’accéder à la plateforme tout en sécurisant leurs données personnelles.
<br/>Ils sont essentiellement nécessaires à la navigation et s’avèrent indispensables pour les applications mobiles.
<br/>Ils donnent accès à toutes les fonctionnalités du site ou de l’application et reconnaissent les utilisateurs à chacune de leurs visites.
<br/>Ils aident à la mémorisation des mots de passe ainsi que des informations renseignées dans un formulaire rempli sur le site.
<br/>Les cookies techniques ont la particularité de sécuriser la navigation.
<br/>À cet effet, ils peuvent par exemple demander aux visiteurs de se reconnecter à l’espace adhérent après une longue période d’inactivité.
<br/>L’accès au site n’est toutefois pas conditionné par l’utilisation de cookies, ce qui veut dire qu’il est possible de supprimer les cookies tout en accédant à la plateforme.
<br/>Ces cookies ne durent pas longtemps. Ils peuvent être supprimés à travers les paramètres du navigateur.
<br/>Dans ce cas, l’utilisateur risque de ne plus accéder au site, à l’application ou aux services de la plateforme web.
<br/>Leur durée de vie peut se limiter à quelques heures ou à une année au maximum.
<br/><u>Les cookies de mesure d’audience :</u><br/>
<br/>Ces cookies analysent le taux de fréquentation des internautes et identifient les éventuels soucis de navigation.
<br/>C’est grâce à eux que les plateformes en ligne peuvent améliorer la qualité de leurs services ainsi que leur accessibilité.
<br/>Les cookies de mesure d’audience servent pratiquement à mesurer l’efficacité des actions commerciales.
<br/>Il est également possible de refuser l’utilisation de ces cookies. Leur durée de vie est limitée à treize mois.
<br/><u>Les cookies de partage :</u>
<br/>Ces cookies de partage servent à optimiser l’interactivité sur les réseaux sociaux.
<br/>Ils favorisent le partage de contenus et d’avis sur le site ou l’application.
<br/>Ils s’affichent dès l’activation de certains paramètres, comme c’est le cas pour les boutons « j’aime » et « partager » sur les réseaux sociaux.
<br/>Certains boutons permettent même d’identifier les utilisateurs, dès lors que ces derniers ne s’en sont pas servis lors de leur dernière visite.
<br/>Les internautes doivent toutefois rester vigilants sur ce type de site, car leur vie privée est souvent menacée par les différentes formes de piratage.
<br/>De ce fait, il convient de consulter les politiques de protection de la vie privée avant de naviguer sur le site et d’accepter les cookies de partage.
<br/><u>Strictement nécessaires(exemptés de consentement) :</u>
<br/>Ces cookies sont indispensables pour naviguer sur notre Site en profitant de toutes ses fonctionnalités et permettent notamment de simplifier et sécuriser votre navigation sur notre Site.
<br/>Ils peuvent être activés par nous ou par des tiers dont les services sont utilisés sur notre Site.
<br/>Sans ces cookies, certaines ou toutes les fonctionnalités du Site seront susceptibles de ne pas fonctionner correctement et vous ne pourrez pas utiliser notre site ni nos services demandés par vous aux fins de l’utilisation de notreSite.
<br/>Ces cookies ne relèvent pas d’un choix et ne peuvent pas être refusés.
<br/><u>Ces cookies permettent notamment :</u>
<br/>De vous reconnaître et de vous connecter automatiquement grâce à la mémorisation de vos informations remplies dans les formulaires de notre Site (inscription ou accès à votre compte) ou à des produits, services ou informations que vous avez choisis.
<br/>D’adapter la présentation de notre Site aux préférences d'affichage de votre terminal (résolution d'affichage, système d'exploitation utilisé, etc.).
<br/>De vous donner accès à des espaces réservés et personnels de notre Site, grâce à vos identifiants.

