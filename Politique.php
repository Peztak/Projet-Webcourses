<h2><b><u>Politique de Confidentialité</u></b></h2>
<h3><b><u>1. BUTS ET OBJECTIFS</u></b></h3>
<p><br>Les employés et personnel de Webcourses et de ses
sociétés associées ont le devoir de maintenir la
confidentialité des informations qu’ils reçoivent dans le cadre de leur
emploi ou engagement. La présente politique de confidentialité documente
les obligations et obligations des employés et personnel de Webcourses en matière de confidentialité et de non-divulgation.</br>
<br>Les employés et le personnel de Webcourses, dans le cadre de leur
emploi et engagement, doivent prendre connaissance de Webcourses ou de tiers divulgués à Webcourses qui n’est pas
généralement connu. Cela peut inclure des informations qui peuvent
mettre en péril les intérêts de Webcourses. Il peut également inclure
la divulgation de secrets commerciaux qui pourraient nuire aux intérêts de Webcourses.</br>
<br>Tous les employés et le personnel de Webcourses ont le devoir de conserver les
informations strictement confidentielles et de les utiliser uniquement à des fins
conforme à la loi.</br></p>
<h3><b><u>2. OBJET</u></b></h3>
<p><br>La présente politique de confidentialité a pour objet de définir les principes qui
doit être observé par tous ceux qui travaillent avec Webcourses et ont accès aux
informations confidentielles.</br>
<br>La présente politique, le cas échéant, doit être lue conjointement avec une
lettre de nomination et/ou contrat de travail applicable aux 
employés et le personnel de Webcourses, ainsi que d’autres règles, politiques et procédures de travail
applicable aux employés et au personnel de Webcourses.</br></p>
<h3><b><u>3. RENSEIGNEMENTS CONFIDENTIELS</u></b></h3>
<p><br>Les renseignements confidentiels comprennent tout renseignement qui n’est pas connu du public.
Elle peut concerner la technologie, les affaires, les finances, les transactions ou d’autres
société. Il comprend des informations qui sont commercialement précieux tels que le secret commerciale
ou les renseignements commerciaux, ainsi que les renseignements personnels.</br>
<br>Voici quelques exemples de renseignements confidentiels:
document, découverte, invention, amélioration, fascicule de brevet,
formules, plans, idées, livres, comptes, données, rapports, ébauches de
documents de toutes sortes, correspondance, renseignements sur les clients, listes et dossiers,
décisions, informations sur les employés, stratégies, dessins,
les recommandations, les conceptions, les précédents, les politiques et les procédures du bureau;
l’information budgétaire et financière sous quelque forme que ce soit, c.-à-d. physique, électronique, électromagnétique ou autre.</br>
<br>Les informations confidentielles relatives aux inventions non publiées peuvent être particulièrement
sensible. La divulgation d’une invention avant le dépôt d’une demande de brevet
cause une perte irréversible des droits de propriété intellectuelle au propriétaire de
l'invention. Même après le dépôt d’une demande de brevet, il faut veiller à ne pas
divulguer les améliorations apportées à l’invention. La protection des secrets commerciaux sera également
perdu par la divulgation ouverte du secret.</br></p>
<h3><b><u>4. PRINCIPES</u></b></h3>
<p><br>Webcourses s’attend à ce que tous ses employés et le personnel gérent toutes les
informations confidentielles de manière sensible et professionnelle. Les employés et le personnel de Webcourses ont l’obligation de ne pas
tenter d’accéder à des informations qu’ils ne sont pas autorisés à avoir.
Webcourses, cependant, reconnaît l’importance d’une culture ouverte avec une
communication claire et responsable. Webcourses souhaite maintenir la
sécurité personnelle et organisationnelle et attend de tous les employés et le personnel à
traiter les informations confidentielles de manière à assurer la
sécurité.</br>
<br>Le but de la confidentialité est essentiellement double. Premièrement, elle protège
les informations sensibles ou confidentielles de Webcourses et de ses clients et
clientes. Deuxièmement, afin que Webcourses soit efficace,les employés et le personnel 
de Webcourses doivent être en mesure de partager l’information et les connaissances;
et donc la confidentialité est une condition de confiance.
La meilleure protection contre les atteintes à la confidentialité est de garder le numéro
des employés et du personnel qui ont accès à des renseignements sensibles.</br>
<br>Divulgation intentionnelle, répétée, accidentelle ou non autorisée
des renseignements confidentiels de tout membre du personnel seront assujettis à des
mesure. Toute mesure disciplinaire tiendra compte de la confidentialité et de la
nature délicate possible de l’information et veillera à ce qu'il n’y a pas d’autres atteintes à la confidentialité.</br></p>
<h3><b><u>5. MAINTIEN DE LA CONFIDENTIALITÉ ET NON-DIVULGATION</u></b></h3>
<p><br>Les employés et personnel de Webcourses:</br>
<ol>
    <li>doivent garder confidentiels tous les renseignements confidentiels.</li>
    <li>peuvent utiliser des renseignements confidentiels uniquement aux fins suivantes:
exercer ses fonctions à titre d’employé du Webcourses.</li>
    <li>ne peuvent divulguer des renseignements confidentiels qu’aux personnes qui:
savent que les renseignements confidentiels doivent demeurer confidentiels
et qui ont besoin de le savoir (mais seulement dans la mesure où chaque
personne a besoin de le savoir).</li>
</ol>
<br>Obligation de l’employé et du personnel de maintenir la confidentialité et
la non-divulgation ne s’applique pas aux renseignements confidentiels qui doivent
être divulgué par l’employé en vertu d’une ordonnance d’un tribunal ou
autorité. L’employé ou la personne informera rapidement la Société de toute
obligation de permettre à la Société de prendre les mesures
jugée apte par la Société dans les circonstances.</br>
<br>À la fin de la période d’emploi, les employés et le personnel
doivent retourner à Webcourses:</br>
<ol>
    <li>tous les renseignements confidentiels sous forme matérielle.</li>
    <li>les parties de toutes les notes et autres documents sous quelque forme que ce soit,
fondée sur des renseignements confidentiels ou intégrant des renseignements confidentiels.</li>
    <li>toutes les copies de renseignements et notes confidentiels et autres
documents fondés sur des renseignements confidentiels ou incorporant des renseignements confidentiels.</li>
    <li>tous les biens et actifs de Webcourses,
en la possession ou le contrôle de l’employé ou du personnel de Webcourses.
L’obligation de préserver la confidentialité et la non-divulgation se poursuivra.
même après la fin de la période d’emploi ou d’engagement à l’égard de
toutes les informations confidentielles.</li>
</ol>
<br>Tout employé qui contrevient à l’obligation de confidentialité et à la non-divulgation, 
tout en étant employé par Webcourses sera sanctionné, et dans des
cas graves, congédié. Tout ex-employé jugé en violation de cette
l’obligation de confidentialité peut faire l’objet de poursuites
selon les circonstances de l’infraction, y compris l’annulation
/ retrait d’une partie ou de la totalité des prestations si elles sont accordées à l’ex-employé par l'entreprise.</br>
<br>La présente politique s’appliquera en conjonction avec le contrat de travail ou la lettre.
de nomination pour les employés et le personnel de Webcourses.</br></p>
<h3><b><u>6. BON À SAVOIR</u></b></h3>
<p><br>Les informations confidentielles ne doivent être divulguées que sur la base du «bon à savoir»,
seulement lorsque les renseignements sont nécessaires à l’employé pour l’exécution efficace de ses
ses fonctions d’emploi.</br></p>
<h3><b><u>7. CIRCONSTANCES DANS LESQUELLES DES RENSEIGNEMENTS PEUVENT ÊTRE DIVULGUÉS</u></b></h3>
<p>
<ol>
    <li>Avec le consentement écrit de son supérieur hiérarchique de
Niveau 4 et à une fin particulière.</li>
    <li>Si les renseignements sont exigés en vertu d’une ordonnance de la Cour ou d’une loi
de l'autorité, l’employé ou la personne informera rapidement la Société de ce qui suit:
une telle exigence pour permettre à la Société de prendre les mesures nécessaires
comme jugé approprié par la Société dans les circonstances.</li>
    <li>Lorsque la divulgation peut être justifiée à toute autre fin. C’est habituellement
pour la protection du public et est susceptible de la prévention et la détection des crimes graves.
la police doit être examinée attentivement.</li>
</ol>
<br>L’employé de Webcourses doit être en mesure de justifier toute décision lorsque
l’information a été divulguée. </br></p>
<h3><b><u>8. STOCKAGE DES DONNÉES</u></b></h3>
<p><br>Aucun document écrit contenant des informations confidentielles ne doit être laissé
visible où il peut être lu par n’importe qui. </br> 
<br>Cela comprend les messages téléphoniques, les imprimés informatiques, les lettres et autres documents. Tout le matériel
contenant des renseignements confidentiels doivent être conservés dans un
environnement. </br>
<br>Les mesures de sécurité doivent être prises conformément aux
la politique et les procédures de Webcourses.</br></p>
<h3><b><u>9. LES MEDIAS</u></b></h3>
<p><br>Les informations confidentielles ne doivent pas être transmises aux membres de la
presse ou autres communications médiatiques sans le consentement écrit du
supérieur hiérarchique de niveau 4 minimum et pour un
but.</br>
<br>Toutes les demandes provenant des médias doivent être traitées dans le cadre de la procédure de Webcourses pour le traitement des demandes de médias.</br></p>
<h3><b><u>10. SUPPRESIONS DES INFORMATIONS</u></b></h3>
<p><br>Tous les supports contenant des informations confidentielles doivent être éliminés
de manière à ce que les renseignements ne soient pas divulgués à une
personne non autorisée.</br></p>
